package com.ut.scf.pojo;

import java.util.Date;

public class FlowProject {
    
    private String proId;

    private String corpId;
    
    private String projectName;

    private String apId;
    
    private Date createTime;

    
    private String createUserId;

    
    private Byte creditMode;

    
    private Byte proStatus;

    
    public String getApId() {
		return apId;
	}


	public void setApId(String apId) {
		this.apId = apId;
	}


	public String getCorpId() {
		return corpId;
	}


	public void setCorpId(String corpId) {
		this.corpId = corpId;
	}


	public String getProId() {
        return proId;
    }

    
    public void setProId(String proId) {
        this.proId = proId;
    }

    
    public String getProjectName() {
        return projectName;
    }

    
    public void setProjectName(String projectName) {
        this.projectName = projectName;
    }

    
    public Date getCreateTime() {
        return createTime;
    }

    
    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    
    public String getCreateUserId() {
        return createUserId;
    }

    
    public void setCreateUserId(String createUserId) {
        this.createUserId = createUserId;
    }

    
    public Byte getCreditMode() {
        return creditMode;
    }

    
    public void setCreditMode(Byte creditMode) {
        this.creditMode = creditMode;
    }

    
    public Byte getProStatus() {
        return proStatus;
    }

    
    public void setProStatus(Byte proStatus) {
        this.proStatus = proStatus;
    }
}