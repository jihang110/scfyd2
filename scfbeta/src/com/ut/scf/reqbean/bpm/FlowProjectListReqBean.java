package com.ut.scf.reqbean.bpm;

import com.ut.scf.reqbean.PageReqBean;

/**
 * @author jihang
 *	flow_project表查询
 */
public class FlowProjectListReqBean extends PageReqBean{
	/**
	 * 企业Id
	 */
	private String corpId;
	/**
	 * 项目名称
	 */
	private String projectName;
	
	private String proId;
	
	private String apId;
	
	private Byte creditMode;
	
	private Byte proStatus;
	
	private Byte state;
	/**
	 * 是否分页，0：否，1：是，默认为0.
	 */
	private Integer isPage = 0;
	
	
	public Integer getIsPage() {
		return isPage;
	}
	public void setIsPage(Integer isPage) {
		this.isPage = isPage;
	}
	public String getCorpId() {
		return corpId;
	}
	public void setCorpId(String corpId) {
		this.corpId = corpId;
	}
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	public String getProId() {
		return proId;
	}
	public void setProId(String proId) {
		this.proId = proId;
	}
	public String getApId() {
		return apId;
	}
	public void setApId(String apId) {
		this.apId = apId;
	}
	public Byte getCreditMode() {
		return creditMode;
	}
	public void setCreditMode(Byte creditMode) {
		this.creditMode = creditMode;
	}
	public Byte getProStatus() {
		return proStatus;
	}
	public void setProStatus(Byte proStatus) {
		this.proStatus = proStatus;
	}
	public Byte getState() {
		return state;
	}
	public void setState(Byte state) {
		this.state = state;
	}
	
}
